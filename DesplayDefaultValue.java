public class DesplayDefaultValue {
    static int a;
    static float b;
    static double c;
    static char d;
    static long e;
    static byte f;

    public static void main(String[] args) {
        System.out.println(a);
        System.out.println(b);
        System.out.println(f);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
